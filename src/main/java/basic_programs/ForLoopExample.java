package basic_programs;

public class ForLoopExample {
	public static void main(String[] args) {
		for (int i = 1; i <= 3; i++) {
			System.out.println("Loop: "+i);
		}

	}

}

// i = 1 >      true >> o/p: Loop: 1
// i = 2 >      true >> o/p: Loop: 2
// i = 3 >      true >> o/p: Loop: 3
// i = 4 >     false >> o/p: -




